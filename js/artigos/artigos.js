// Function to get query parameters from the URL
function getQueryParams() {
    const params = {};
    const queryString = window.location.search;
    const urlParams = new URLSearchParams(queryString);
    
    urlParams.forEach((value, key) => {
        params[key] = value;
    });

    return params;
}

